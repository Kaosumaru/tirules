<p>During the action phase, the active player may perform a strategic action to resolve the primary ability on their strategy card.</p>
    <ol class="lrr">
    <li>After the active player resolves the primary ability on their strategy card, each other player, beginning with the player to the left of the active player and proceeding clockwise, may resolve that strategy card&rsquo;s secondary ability.</li>
    <ol><li>Players do not have to resolve the secondary abilities of the active player&rsquo;s strategy card.</li></ol>
    <li>After each player has had an opportunity to resolve a strategy card&rsquo;s secondary ability, the active player exhausts their strategy card so that it is facedown &ndash; this indicates that they cannot use this card again this round and is a reminder that they can now pass during one of their later turns.</li>
    <ol><li>During three&ndash; and four&ndash;player games, a player must resolve the strategic action on each of their chosen strategy cards before they can pass.</li></ol>
    <li>When a player is resolving either the primary or secondary abilities from a strategy card, that player resolves each of the ability&rsquo;s effects from top to bottom.</li>
    </ol>


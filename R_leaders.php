<h1>Rules Reference</h1>
<style> .lrr > li:before, .lrr > h2 > li:before {content: '51.' counter(item, decimal);} </style>
<p>Each player has several faction&ndash;specific leader cards that represent characters with unique abilities.</p>
    <ol class="lrr">
	<li>Each faction has three leaders; one agent, one commander, and one hero.</li>
    <ol><li>The Nomad&rsquo;s <sc>The Company</sc> faction ability grants them two additional agents, for a total of five leaders.</li></ol>
    <li>A player&rsquo;s leaders are placed on their leader sheet during setup.</li>
    <ol>
    	<li>Each leader card is placed on the slot that matches its type. The leader cards are placed so that the hash mark symbols in the upper right are faceup and match the slot that the card is placed on.</li>
    	<li>The two additional Nomad agents are placed in the Nomad player&rsquo;s play area readied side up.</li>
    </ol>
    <h2><li>Agents</li></h2>
    <li>An agent does not need to be unlocked and begins the game in a readied state. They can be exhausted by resolving their abilities, and they ready during the <b>Ready Cards</b> step of the status phase.</li>
    <h2><li>Commanders</li></h2>
    <li>A commander must be unlocked to use its abilities. A player unlocks their commander if they fulfill the conditions listed after the &ldquo;Unlock&rdquo; header.</li>
    <ol>
    	<li>Each faction&rsquo;s commander has a unique &ldquo;Unlock&rdquo; condition.</li>
    	<li>After a player fulfills the unlock condition of their commander, they flip it over to its unlocked side.</li>
    	<li>A commander&rsquo;s unlock conditions cannot be met while an ability or game effect is being resolved. That is, pending abilities or partially resolved game effects must be completed before checking if conditions are met.</li>
    	<li>A commander cannot flip to its locked side after it is unlocked, even if its owner no longer meets the unlock conditions.</li>
    </ol>
    <li>A commander cannot be exhausted.</li>
    <li>The <i>Alliance</i> promissory note allows a player to share their commander&rsquo;s ability with another player.</li>
    <ol><li>A commander&rsquo;s owner can still use their commander&rsquo;s ability, even if another player has their <i>Alliance</i> promissory note.</li></ol>
    <h2><li>Heroes</li></h2>
    <li>A hero needs to be unlocked to use their abilities. A player unlocks their hero if they fulfill the conditions listed after the &ldquo;Unlock&rdquo; header.</li>
    <ol>
    	<li>The &ldquo;Unlock&rdquo; condition for each hero is to have three scored objectives; these can be any combination of secret objectives and public objectives.</li>
    	<li>Victory points do not count toward unlocking heroes.</li>
    	<li>After a player fulfills the unlock condition of their hero, they flip it to its unlocked side.</li>
    	<li>A hero cannot flip to its locked side after it is unlocked.</li>
    </ol>
    <li>A hero cannot be exhausted.</li>
    <li>A hero is purged after its abilities are resolved.</li>
    <ol><li>The Titans of Ul&rsquo;s hero is not purged; it is attached to the planet Elysium instead.</li></ol>
    </ol>

<h1>Notes</h1>
    <ol class="note">
    <li>A commander&rsquo;s ability cannot be applied to the instance of the effect that unlocked it.</li>
    <li>A commander may be unlocked between two abilities that occur in the same timing window.</li>
    <li>For notes about a leader of a specific faction, see that faction&rsquo;s page.</li>
    </ol>


<h1>Rules Reference</h1>
<style> .lrr > li:before, .lrr > h2 > li:before {content: '88.' counter(item, decimal);} </style>
<p>A system tile represents an area of the galaxy. Players place system tiles during setup to create the game board.</p>
    <ol class="lrr">
    <li>The back of each system tile is colored green, blue, or red.</li>
    <li>System tiles with a green&ndash;colored back are home systems and faction&ndash;specific tiles. Each home system is unique to one of the game&rsquo;s factions.</li>
    <li>System tiles with a blue&ndash;colored back each contain one or more planets.</li>
    <li>System tiles with a red&ndash;colored back are anomalies or are systems that do not contain planets.</li>
    <li>Planets are located in systems. Ground forces and structures are usually placed on planets.</li>
    <li>Any area on a system tile that is not a planet is space. Ships are usually placed in the space area.</li>
    <li>Double&ndash;sided tiles that have lines crossing from one edge to another are hyperlane tiles. Hyperlane tiles are not systems.</li>
    </ol>


<h1>Rules Reference</h1>
<style> .lrr > li:before, .lrr > h2 > li:before {content: '98.' counter(item, decimal);} </style>
<p>The first player to gain 10 victory points wins the game.</p>
    <ol class="lrr">
    <li>Players gain victory points in a variety of ways. A common way that a player can gain victory points is by scoring objectives.</li>
    <li>Each player uses the victory point track to indicate the number of victory points they have gained.</li>
    <ol><li>If the players are using the 14&ndash;space side of the victory point track, the game ends and a player wins when they have 14 victory points instead of 10.</li></ol>
    <li>Each player places one of their control tokens on space &ldquo;0&rdquo; of the victory point track during setup.</li>
    <li>When a player gains a victory point, they advance their control token a number of spaces along the victory point track equal to the number of victory points gained.</li>
    <ol><li>A player&rsquo;s control token must always be on the space of the victory point track that shows a number that matches the number of victory points that player has gained during the game. A player cannot have more than 10 victory points.</li></ol>
    <li>If an ability refers to the player with the &ldquo;most&rdquo; or &ldquo;fewest&rdquo; victory points, and more than one player is tied in that respect, the effect applies to all of the tied players.</li>
    <li>If a player gains a victory point from a law, and that law is discarded, that player does not lose that victory point.</li>
    <li>The game ends immediately when one player has 10 victory points. If multiple players would simultaneously gain their 10<sup>th</sup> victory point, the player who is earliest in initiative order among those players is the winner; if this occurs when players do not have strategy cards, the player who is nearest the speaker (including the speaker) in clockwise order is the winner.</li>
    <li>If the game ends because the speaker cannot reveal an objective card, the player with the most victory points is the winner. If one or more players are tied for having the most victory points, the tied player who is first in initiative order is the winner.</li>
    </ol>


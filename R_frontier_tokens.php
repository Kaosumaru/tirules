    <ol class="lrr">
	<li>Frontier tokens are placed on the game board during setup. One frontier token is placed in each system that does not contain any planets.</li>
    <ol>
	    <li>Frontier tokens are not placed on hyperlane tiles.</li>
	    <li>A system cannot have more than one frontier token.</li>
	    <li>Frontier tokens are placed in anomalies that do not have planets.</li>
	    <li>A frontier token is placed in the Creuss gate system.</li>
    </ol>
    </ol>

<h1>Notes</h1>
    <ol class="note">
    <li>Players may explore space areas that contain frontier tokens if they own the <i>Dark Energy Tap</i> technology or if another game effect allows them to.</li>

<p>The <i>Politics</i> strategy card allows players to draw action cards. Additionally, the active player chooses a new speaker and looks at cards in the agenda deck. This card&rsquo;s initiative value is &ldquo;3&rdquo;.</p>
    <ol class="lrr">
    <li>During the action phase, if the active player has the <i>Politics</i> strategy card, they can perform a strategic action to resolve that card&rsquo;s primary ability.</li>
    <li>To resolve the primary ability on the <i>Politics</i> strategy card, the active player resolves the following effects in order:</li>
    <ol class="roman">
        <li>The active player chooses any player that does not have the speaker token. The active player may choose themselves as long as they do not have the speaker token. The chosen player places the speaker token in their play area; they are now the speaker.</li>
        <li>The active player draws two action cards.</li>
        <li>The active player secretly looks at the top two cards of the agenda deck. Then, that player places each card on either the top or the bottom of the deck. If they place both cards on either the top or bottom, they can place them in any order.</li>
    </ol>
    <li>After the active player resolves the primary ability of the <i>Politics</i> strategy card, each other player, beginning with the player to the left of the active player and proceeding clockwise, may spend one command token from their strategy pool to draw two action cards.</li>
    </ol>

<h1>Notes</h1>
    <ol class="note">
    <li>When resolving the primary ability of the <i>Politics</i> card, the active player cannot show the agenda cards they drew to any other player.</li>
    </ol>


<p>The custodians token begins each game on Mecatol Rex. The token represents the caretakers that safeguard the seat of the empire until such time as the galactic council can be reconvened.</p>
    <ol class="lrr">
	<li>Units can move into the system that contains Mecatol Rex following normal rules; however, players cannot commit ground forces to land on Mecatol Rex until the custodians token is removed from the planet.</li>
    <li>Before the <b>Commit Ground Forces</b> step of an invasion, the active player can remove the custodians token from Mecatol Rex by spending six influence. Then, that player must commit at least one ground force to land on the planet.</li>
    <ol><li>If a player cannot commit ground forces to land on Mecatol Rex, they cannot remove the custodians token.</li></ol>
    <li>When a player removes the custodians token from Mecatol Rex, they take the token from the game board and place it in their play area. Then, they gain one victory point.</li>
    <li>After a player removes the custodians token from Mecatol Rex, the agenda phase is added to all subsequent game rounds, including the game round during which the custodians token was removed from Mecatol Rex.</li>
    </ol>

<h1>Notes</h1>
    <ol class="note">
    <li>The custodians token is not an attachment.</li>
    </ol>


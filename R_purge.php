<p>Purge is a cost that permanently removes a component from the game. If an ability requires that its component is purged, that component can only be used once per game.</p>
    <ol class="lrr">
    <li>If an ability instructs a player to purge a component, that component is removed from the game and returned to the box.</li>
    <li>Purged components cannot be used or otherwise returned to the game by any means.</li>
    <li>When a player is instructed to purge a component, that component is purged even if its ability was only partially resolved.</li>

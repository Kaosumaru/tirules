<h1>Rules Reference</h1>
<style> .lrr > li:before, .lrr > h2 > li:before {content: '87.' counter(item, decimal);} </style>
<p>Some units have the <sc>Sustain Damage</sc> ability. Immediately before a player assigns hits to their units, that player can use the <sc>Sustain Damage</sc> ability of any of their units in the active system.</p>
    <ol class="lrr">
    <li>For each <sc>Sustain Damage</sc> ability that a player uses, one hit produced by another player&rsquo;s units is canceled. Then, each unit using this ability is placed on its side to indicate that it is damaged.</li>
    <li>A damaged unit does not have reduced capabilities and is functionally the same as an undamaged unit, except that it cannot use the <sc>Sustain Damage</sc> ability.</li>
    <li>A damaged unit cannot use the <sc>Sustain Damage</sc> ability until it is repaired during the status phase or by another game effect.</li>
    <li>A unit can use its <sc>Sustain Damage</sc> ability any time a hit is produced against it. This includes hits produced during combat and from unit abilities such as the <sc>Space Cannon</sc> ability.</li>
    <ol><li>A unit can only use the <sc>Sustain Damage</sc> ability if it is eligible to be hit. For example, a player cannot use a dreadnought&rsquo;s <sc>Sustain Damage</sc> ability to cancel a hit from <sc>Anti&ndash;Fighter Barrage.</sc></li></ol>
    <li>The <sc>Sustain Damage</sc> ability cannot be used to cancel an effect that directly destroys a unit.</li>
    <li>The Barony of Letnev&rsquo;s <i>Non&ndash;Euclidean Shielding</i> faction technology allows the Letnev player&rsquo;s units with the <sc>Sustain Damage</sc> ability to cancel up to two hits instead of one.</li>
    </ol>


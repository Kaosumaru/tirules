<style> .lrr > li:before, .lrr > h2 > li:before {content: '93.' counter(item, decimal);} </style>
<p>A trade good represents a player&rsquo;s buying and trading power beyond their planet&rsquo;s raw resources.</p>
    <ol class="lrr">
	<li>Trade goods and commodities are represented by opposite sides of the same token.</li>
    <li>When a player gains a trade good, they take a trade good token from the supply and place it on the trade good area on their command sheet, making sure the trade good side is faceup.</li>
    <li>A player can spend trade goods at any time during the game.</li>
    <li>A player can spend a trade good in one of the following ways:</li>
    <ol>
    	<li>In place of spending one resource.</li>
    	<li>In place of spending one influence. However, trade goods cannot be spent to cast votes during the agenda phase.</li>
    	<li>To resolve an effect that specifically requires that a trade good be spent.</li>
    </ol>
    <li>A player can exchange their trade goods with other players during a transaction.</li>
    <li>When a player receives a commodity token from another player, the player who received that token places it in their trade good area with the trade good side of the token faceup.</li>
    <ol><li>That token is no longer a commodity token; it is a trade good token.</li></ol>
    <li>Trade good tokens come in values of one and three. A player can swap between these tokens as necessary.</li>
    </ol>


<p>The <i>Diplomacy</i> strategy card can be used to preemptively prevent other players from activating a specific system. It can also be used to ready planets. This card&rsquo;s initiative value is &ldquo;2&rdquo;.</p>
    <ol class="lrr">
	<li>During the action phase, if the active player has the <i>Diplomacy</i> strategy card, they can perform a strategic action to resolve that card&rsquo;s primary ability.</li>
	<li>To resolve the primary ability on the <i>Diplomacy</i> strategy card, the active player chooses a system that contains a planet they control other than the Mecatol Rex system; each other player places one command token from their reinforcements in that system. Then, the active player readies any two of their exhausted planets.</li>
    <ol>
    	<li>If a player has no command tokens in their reinforcements, that player places one command token of their choice from their command sheet.</li>
    	<li>If a player already has a command token in the chosen system, they do not place a command token there.</li>
    </ol>
    <li>After the active player resolves the primary ability of the <i>Diplomacy</i> strategy card, each other player, beginning with the player to the left of the active player and proceeding clockwise, may spend one command token from their strategy pool to ready up to two exhausted planets they control.</li>
    </ol>


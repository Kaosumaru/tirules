<p>The <i>Technology</i> strategy card allows players to research new technology. This card&rsquo;s initiative value is &ldquo;7&rdquo;.</p>
    <ol class="lrr">
	<li>During the action phase, if the active player has the <i>Technology</i> strategy card, they can perform a strategic action to resolve that card&rsquo;s primary ability.</li>
    <li>To resolve the primary ability on the <i>Technology</i> strategy card, the active player can research one technology of their choice. Then, they may research one additional technology of their choice by spending six resources.</li>
    <li>After the active player resolves the primary ability of the <i>Technology</i> strategy card, each other player, beginning with the player to the left of the active player and proceeding clockwise, may research one technology of their choice by spending one command token from their strategy pool and four resources.</li>
    </ol>


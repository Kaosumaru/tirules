<p>The speaker is the player who has the speaker token.</p>
    <ol class="lrr">
    <li>During the strategy phase, the speaker is the first player to choose a strategy card.</li>
    <li>During the agenda phase, the speaker reveals the top agenda card from the agenda deck before each vote. The speaker is always the last player to vote and decides which outcome to resolve if the outcomes are tied.</li>
    <li>During setup, the speaker prepares the objectives.</li>
    <li>During the status phase, the speaker reveals a public objective.</li>
    <li>A random player gains the speaker token during setup before the game begins.</li>
    <li>During the action phase, if a player resolves the primary ability on the <i>Politics</i> strategy card, that player chooses any player other than the current speaker to gain the speaker token.</li>
    <li>If the speaker is eliminated from the game, the speaker token is passed to the player to the speaker&rsquo;s left.</li>
    </ol>

<h1>Notes</h1>
    <ol class="note">
    <li>During the strategy and agenda phases, the speaker will have the first opportunity to resolve abilities during each timing window.</li>
    </ol>


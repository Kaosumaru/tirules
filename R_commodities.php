<h1>Rules Reference</h1>
<style> .lrr > li:before, .lrr > h2 > li:before {content: '21.' counter(item, decimal);} </style>
<p>Commodities represent goods that are plentiful for their own faction and are desired by other factions. A commodity has no inherent game effects, but converts into a trade good if given to or received from another player.</p>
    <ol class="lrr">
    <li>Commodities and trade goods are represented by opposite sides of the same token.</li>
    <li>The commodity value on a player&rsquo;s faction sheet indicates the maximum number of commodities that player can have.</li>
    <li>When an effect instructs a player to replenish commodities, that player takes the number of commodity tokens necessary so that the amount of commodities that player has equals the commodity value on their faction sheet. Then, those tokens are placed faceup in the commodity area of that player&rsquo;s faction sheet.</li>
    <li>When a player replenishes commodities, that player takes the commodity tokens from the supply.</li>
    <li>Players can trade commodities following the rules for transactions. When a player receives a commodity from another player, the player who received that token converts it into a trade good by placing it in the trade good area of their command sheet with the trade good side faceup.</li>
    <ol>
        <li>That token is no longer a commodity token; it is a trade good token.</li>
        <li>A player can trade commodity tokens before resolving a game effect that allows them to replenish commodities.</li>
        <li>If a game effect instructs a player to convert a number of their own commodities to trade goods, those trade goods are not treated as being gained for the purpose of triggering other abilities.</li>
    </ol>
    <li>Any game effect that instructs a player to give a commodity to another player causes that commodity to be converted into a trade good.</li>
    <li>A player cannot spend commodities unless otherwise specified; a player can only trade them during a transaction.</li>
    <li>Commodity tokens come in values of one and three. A player can swap between these tokens as necessary.</li>
    </ol>

<h1>Notes</h1>
    <ol class="note">
    <li>A player starts the game with zero commodities.</li>
    <li>If a player has their maximum number of commodities, and is instructed to replenish their commodities, they will take no commodities. However, they will still trigger any &ldquo;when [this] player replenishes commodities&rdquo; effects, most notably the <i>Trade Agreement</i> promissory note.</li>
    </ol>


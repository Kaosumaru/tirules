<p>The <i>Construction</i> strategy card allows players to construct structures on planets they control. This card&rsquo;s initiative value is &ldquo;4&rdquo;.</p>
    <ol class="lrr">
	<li>During the action phase, if the active player has the <i>Construction</i> strategy card, they can perform a strategic action to resolve that card&rsquo;s primary ability.</li>
    <li>To resolve the primary ability on the <i>Construction</i> strategy card, the active player may place either one PDS or one space dock on a planet they control. Then, that player may place an additional PDS on a planet they control.</li>
    <ol>
    	<li>The structures can be placed on the same planet or different planets.</li>
    	<li>The structures can be placed in any systems, regardless of whether the player has a command token in the system or not.</li>
    </ol>
    <li>After the active player resolves the primary ability of the <i>Construction</i> strategy card, each other player, beginning with the player to the left of the active player and proceeding clockwise, may spend one command token from their strategy pool and place that command token in any system. If that player already has a command token in that system, the spent token is returned to their reinforcements instead. Then, that player places either one PDS or one space dock on a planet they control in that system.</li>
    <li>When a player places either a PDS or space dock using the <i>Construction</i> strategy card, they take that PDS or space dock from their reinforcements.</li>
    <ol><li>If a player does not have the unit in their reinforcements, that player can remove a unit from any system that does not contain one of their command tokens and place that unit in their reinforcements. Then, that player must place the unit on the game board as instructed by the effect that is placing the unit.</li></ol>
    </ol>

<h1>Notes</h1>
    <ol class="note">
    <li>A player may have a maximum of one space dock on each planet.</li>
	<li>A player may have a maximum of two PDS units on each planet.</li>
    <li>Structures cannot be placed on the planet with the <i>Demilitarized Zone</i> exploration card attached.</li>
    </ol>


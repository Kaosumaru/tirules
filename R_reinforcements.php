<p>A player&rsquo;s reinforcements is that player&rsquo;s personal supply of units and command tokens that are not on the game board or otherwise in use.</p>
    <ol class="lrr">
	<li>The components in a player&rsquo;s reinforcements are limited.</li>
    </ol>

<h1>Notes</h1>
    <ol class="note">
    <li>If a player wishes to place a unit, but there are none of that type left in their reinforcements, they may remove a unit of that type from any system that does not contain one of their command tokens and place that instead.</li>
    <li>Command tokens on a player&rsquo;s command sheet are not in that player&rsquo;s reinforcements.</li>
    <li>Captured units are not in any player&rsquo;s reinforcements.</li>
    </ol>

